public class Operatorler2 {
    public static void main(String[] args) {
        int a =30;
        int b =40;
        int c =60;

        // Karsılastırma operatorleri
        // ==, !=, >,<,>=,<=

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);

        if (a >30 || c < 70){
            System.out.println("Kosul saglandi");
        }
    }
}
